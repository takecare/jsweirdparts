// a primitive type is a type that is not an object
// undefined, null, boolean, number (always a floating point), string and symbol
// symbol was introduced in es6

var meaningOfLife = 42;
console.log(typeof meaningOfLife);

var notDefined;
console.log(typeof notDefined);

var empty = null;
console.log(typeof empty);

var answer = true;
console.log(typeof answer);

var name = 'Pirata'
console.log(typeof name);

